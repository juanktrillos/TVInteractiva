    private void bSerialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSerialActionPerformed
        // TODO add your handling code here:
        String com = cSerial.getSelectedItem().toString().toUpperCase();
        if (bSerial.getText().equals("Start Serial")) {
            try {
                arduino.arduinoRX(com, 9600, this);
                bSerial.setText("Stop Serial");
            } catch (Exception ex) {
                Logger.getLogger(JMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                arduino.killArduinoConnection();
                bSerial.setText("Start Serial");
            } catch (Exception ex) {
                Logger.getLogger(JMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bSerialActionPerformed

