    @Override
    public void serialEvent(SerialPortEvent ev) {
        if (arduino.isMessageAvailable()) {
            String cad = arduino.printMessage();
            tSerial.setText(cad);
            if (!bDatabase.isEnabled()) {
                bDatabase.setEnabled(true);
            }
            if (!bFacebook.isEnabled()) {
                bFacebook.setEnabled(true);
            }
        }
    }
